package pl.programa.beerquest.model;

/**
 * class for login model
 * @author Programa.pl
 */
public class Login {

	private String email;

	public Login() {
	}

	public Login(String email) {
		this.email = email;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

}
